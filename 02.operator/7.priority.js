let a = 2;
let b = 3;

// 1. 기본 사칙 연산 우선순위
// 계산 순서: (3 * 4) -> 12 + 2
let result1 = a + b * 4;
console.log(`result1 = ${result1}`); // 14

// 2. 증감 연산자가 포함된 복합 연산
// 계산 순서: 
//   1) a++ 가 현재 값 '2'를 반환 (우선순위는 가장 높음)
//   2) b * 4 계산 (12)
//   3) 2 + 12 계산 (14)
//   4) 마지막에 a가 3으로 증가 (부수 효과)
let result2 = a++ + b * 4;
console.log(`result2 = ${result2}`); // 14
console.log(`최종 a의 값 = ${a}`);     // 3

// 3. 괄호를 통한 우선순위 명시 (가장 권장되는 방식)
// 괄호는 모든 연산자에서 최고의 우선순위를 가지게 된다. 만약 제일 먼저 계산해야한다면 괄호를 활용하여 계산한다.
let result3 = (a + b) * 4; // (3 + 3) * 4
console.log(`result3 = ${result3}`); // 24